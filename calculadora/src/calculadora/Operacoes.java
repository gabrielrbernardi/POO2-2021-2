package calculadora;

public class Operacoes {
	public double soma(double x, double y) {
		return x + y;
	}
	
	public double diferenca(double x, double y) {
		return x - y;
	}
	
	public double multiplica(double x, double y) {
		return x * y;
	}
	
	public double divide(double x, double y) {
		return x / y;
	}
}
